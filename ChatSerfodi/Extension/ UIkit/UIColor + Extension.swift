import UIKit

extension UIColor {
    
    static func bottonRed() -> UIColor {
        .red
    }
    
    static func mainWhite() -> UIColor {
        UIColor(white: 0.95, alpha: 1)
    }
    
    static func buttonDark() -> UIColor {
        .darkGray
    }

    static func textFieldLightGray() -> UIColor {
        .lightGray
    }
    
}

